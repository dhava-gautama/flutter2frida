def extract_apk(apk_path: str, temp_dir: str, preferred_arch: str) -> tuple:
    print(f"[*] Extracting {apk_path}")
    with zipfile.ZipFile(apk_path, "r") as zf:
        zf.extractall(temp_dir)

    lib_dir = os.path.join(temp_dir, "lib")
    if not os.path.exists(lib_dir):
        return None, None

    archs = [a for a in os.listdir(lib_dir) if os.path.isdir(os.path.join(lib_dir, a))]
    if not archs:
        return None, None

    # Preference order based on --arch flag
    pref_map = {
        "x86_64": ["x86_64", "arm64-v8a", "armeabi-v7a", "x86"],
        "arm64":  ["arm64-v8a", "x86_64", "armeabi-v7a", "x86"],
    }
    order = pref_map.get(preferred_arch, pref_map["arm64"])
    selected = next((a for a in order if a in archs), archs[0])
    print(f"[*] archs={archs}  selected={selected}")

    arch_path = os.path.join(lib_dir, selected)
    libapp = os.path.join(arch_path, "libapp.so")
    libflutter = os.path.join(arch_path, "libflutter.so")

    if not os.path.exists(libapp):
        print(f"[!] libapp.so not found in {arch_path}")
        return None, None
    if not os.path.exists(libflutter):
        print(f"[!] libflutter.so not found in {arch_path}")
        return None, None

    return libapp, libflutter


def scan_directory(directory: str) -> tuple:
    libapp = libflutter = None
    for root, _, files in os.walk(directory):
        for f in files:
            if f == "libapp.so":
                libapp = os.path.join(root, f)
            elif f == "libflutter.so":
                libflutter = os.path.join(root, f)
    return libapp, libflutter


