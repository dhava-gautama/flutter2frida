def find_mov_candidates_arm64(
    data: bytes, text_offset: int, text_size: int, scalar: int
) -> list:
    md = Cs(CS_ARCH_ARM64, CS_MODE_ARM)
    candidates = []
    for off in range(text_offset, text_offset + text_size, Config.DISASM_CHUNK_SIZE):
        chunk = data[off: min(off + Config.DISASM_CHUNK_SIZE, text_offset + text_size)]
        for ins in md.disasm(chunk, off):
            if ins.mnemonic == "mov":
                if f"#{scalar}" in ins.op_str or f"#0x{scalar:x}" in ins.op_str:
                    candidates.append(ins.address)
    return candidates


def find_prologue_arm64(data: bytes, addr: int, md: Cs) -> "Optional[int]":
    start = max(addr - Config.PROLOGUE_SEARCH_RANGE, 0)
    chunk = data[start: addr + 8]
    instrs = list(md.disasm(chunk, start))
    target_idx = next((i for i, ins in enumerate(instrs) if ins.address == addr), None)
    if target_idx is None:
        return None
    for idx in range(target_idx, max(target_idx - Config.MAX_INSTRS_BACKWARD, 0), -1):
        ins = instrs[idx]
        is_prologue = (
            (ins.mnemonic == "sub" and "sp, sp," in ins.op_str)
            or (ins.mnemonic == "stp" and "x29, x30" in ins.op_str)
            or (ins.mnemonic == "stp" and "[sp," in ins.op_str)
            or (ins.mnemonic == "str" and "x30," in ins.op_str and "[sp," in ins.op_str)
        )
        if is_prologue:
            if idx == 0:
                return ins.address
            prev = instrs[idx - 1]
            if prev.mnemonic in ("ret", "b", "br", "brk"):
                return ins.address
    return None


def analyze_function_arm64(
    data: bytes, func_addr: int, md: Cs
) -> tuple:
    chunk = data[func_addr: func_addr + Config.ANALYSIS_CODE_SIZE]
    instrs = list(md.disasm(chunk, func_addr))
    saved = set()
    has_frame = False
    has_sp_adj = False
    callee_saved = {f"x{n}" for n in range(19, 30)} | {"x29"}
    for ins in instrs[: Config.ANALYSIS_INSTR_COUNT]:
        if ins.mnemonic == "stp" and "x29, x30" in ins.op_str:
            has_frame = True
        if ins.mnemonic == "sub" and "sp, sp," in ins.op_str:
            has_sp_adj = True
        if ins.mnemonic == "stp":
            for reg in callee_saved:
                if reg in ins.op_str:
                    saved.add(reg)
        if ins.mnemonic in ("bl", "b"):
            break
    return saved, has_frame, has_sp_adj


