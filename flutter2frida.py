# Unified offset finder
# ---------------------------------------------------------------------------

def find_function_offset(
    libflutter_path: str, error_line: int, arch: str, verbose: bool = True
) -> "Optional[FunctionCandidate]":
    binary = lief.parse(libflutter_path)
    if binary is None:
        print("[!] lief could not parse libflutter.so")
        return None

    with open(libflutter_path, "rb") as f:
        data = f.read()

    text = next((s for s in binary.sections if s.name == ".text"), None)
    if not text:
        print("[!] .text section not found")
        return None

    image_base = binary.imagebase

    if verbose:
        print(f"[*] image_base=0x{image_base:x}  arch={arch}  error_line={error_line}")

    if arch == "x86_64":
        return find_function_offset_x86_64(data, text, image_base, error_line, verbose)

    # ARM64 path
    md = Cs(CS_ARCH_ARM64, CS_MODE_ARM)
    candidates_addr = find_mov_candidates_arm64(data, text.offset, text.size, error_line)
    if verbose:
        print(f"[+] MOV candidates: {len(candidates_addr)}")

    results = []
    seen = set()
    for mov_addr in candidates_addr:
        func_addr = find_prologue_arm64(data, mov_addr, md)
        if not func_addr or func_addr in seen:
            continue
        seen.add(func_addr)
        saved, has_frame, has_sp_adj = analyze_function_arm64(data, func_addr, md)
        offset = func_addr - image_base
        mov_offset = mov_addr - func_addr
        c = FunctionCandidate(
            func_addr=func_addr,
            offset=offset,
            saved_regs=saved,
            has_frame_setup=has_frame,
            has_sp_adjust=has_sp_adj,
            mov_offset=mov_offset,
        )
        results.append(c)
        if verbose:
            tag = "[std prologue]" if (has_frame and has_sp_adj) else ""
            print(f"    func=0x{func_addr:08x}  offset=0x{offset:08x}  mov_delta={mov_offset}  {tag}")

    if not results:
        return None
    return max(results, key=lambda r: r.score)


