def generate_frida_script(
    output_path: str,
    offset: int,
    version: str,
    engine_commit: str,
    arch: str,
    apk_info: "Optional[FileInfo]" = None,
    libapp_name: "Optional[str]" = None,
) -> None:
    template = FRIDA_TEMPLATE_X86_64 if arch == "x86_64" else FRIDA_TEMPLATE_ARM64
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    apk_name = apk_info.name if apk_info else (libapp_name or "unknown")
    apk_size = apk_info.size_formatted if apk_info else "N/A"
    apk_sha256 = apk_info.sha256 if apk_info else "N/A"
    offset_str = f"0x{offset:08x}"

    script = template
    script = script.replace("CREATED_TIME",       now)
    script = script.replace("APK_NAME",           apk_name)
    script = script.replace("APK_SIZE",           apk_size)
    script = script.replace("APK_SHA256",         apk_sha256)
    script = script.replace("FLUTTER_VERSION",    version)
    script = script.replace("ENGINE_COMMIT",      engine_commit)
    script = script.replace("ARCH_NAME",          arch)
    script = script.replace("OFFSET_PLACEHOLDER", offset_str)

    with open(output_path, "w") as f:
        f.write(script)


