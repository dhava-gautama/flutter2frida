def detect_arch(libflutter_path: str) -> str:
    binary = lief.parse(libflutter_path)
    if binary is None:
        return "arm64"
    machine = str(binary.header.machine_type)
    if "x86_64" in machine.lower() or "X86_64" in machine:
        return "x86_64"
    if "AARCH64" in machine or "aarch64" in machine:
        return "arm64"
    return "arm64"


