@contextlib.contextmanager
def temp_directory() -> "Iterator[str]":
    tmpdir = tempfile.mkdtemp(prefix="flutter_tls_")
    try:
        yield tmpdir
    finally:
        if os.path.exists(tmpdir):
            shutil.rmtree(tmpdir)


def get_file_info(file_path: str) -> "Optional[FileInfo]":
    if not file_path or not os.path.exists(file_path):
        return None
    sha256 = hashlib.sha256()
    with open(file_path, "rb") as f:
        for chunk in iter(lambda: f.read(Config.CHUNK_SIZE), b""):
            sha256.update(chunk)
    return FileInfo(
        name=os.path.basename(file_path),
        size=os.path.getsize(file_path),
        sha256=sha256.hexdigest(),
    )


def get_snapshot_hash(file_path: str) -> "Optional[str]":
    with open(file_path, errors="ignore") as f:
        result = ""
        for ch in f.read():
            if ch in string.printable:
                result += ch
                continue
            if len(result) >= 32:
                match = re.findall(r"([a-f\d]{32})", result)
                if match:
                    return match[0]
            result = ""
    return None


def fetch_url(url: str, timeout: int = Config.FETCH_TIMEOUT) -> "Optional[str]":
    try:
        with urllib.request.urlopen(url, timeout=timeout) as resp:
            return resp.read().decode("utf-8")
    except (urllib.error.URLError, urllib.error.HTTPError, UnicodeDecodeError):
        return None


def find_flutter_version(
    snapshot_hash: str, enginehash_path: "Optional[str]" = None
) -> "Optional[FlutterVersion]":
    csv = None
    if enginehash_path and os.path.exists(enginehash_path):
        with open(enginehash_path) as f:
            csv = f.read()
    else:
        csv = fetch_url(Config.ENGINEHASH_URL)
    if not csv:
        return None
    for line in csv.strip().split("\n"):
        if snapshot_hash in line:
            parts = line.split(",")
            if len(parts) >= 2:
                return FlutterVersion(version=parts[0], engine_commit=parts[1])
    return None


def get_boringssl_revision(version: str) -> "Optional[str]":
    deps = fetch_url(Config.FLUTTER_DEPS_URL.format(version=version))
    if not deps:
        return None
    match = re.search(r"'dart_boringssl_rev':\s*'([a-f0-9]+)'", deps)
    return match.group(1) if match else None


def fetch_boringssl_file(rev: str) -> "Optional[str]":
    url = Config.BORINGSSL_FILE_URL.format(rev=rev)
    try:
        with urllib.request.urlopen(url, timeout=Config.BORINGSSL_FETCH_TIMEOUT) as resp:
            return base64.b64decode(resp.read()).decode("utf-8")
    except (urllib.error.URLError, urllib.error.HTTPError, UnicodeDecodeError):
        return None


def find_error_line_number(content: str) -> ErrorLineInfo:
    lines = content.split("\n")
    func_start = None
    error_line = None
    for i, line in enumerate(lines, 1):
        if "static bool ssl_crypto_x509_session_verify_cert_chain" in line:
            func_start = i
        if func_start and "OPENSSL_PUT_ERROR" in line:
            error_line = i
            break
    return ErrorLineInfo(func_start_line=func_start, error_line=error_line)


