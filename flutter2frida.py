def find_mov_candidates_x86_64(
    data: bytes, text_offset: int, text_size: int, scalar: int
) -> list:
    from capstone.x86_const import X86_OP_IMM
    md = Cs(CS_ARCH_X86, CS_MODE_64)
    md.detail = True
    candidates = []
    for off in range(text_offset, text_offset + text_size, Config.DISASM_CHUNK_SIZE):
        chunk = data[off: min(off + Config.DISASM_CHUNK_SIZE, text_offset + text_size)]
        for ins in md.disasm(chunk, off):
            if ins.mnemonic not in ("mov", "movabs", "movsx", "movzx"):
                continue
            for op in ins.operands:
                if op.type == X86_OP_IMM and op.imm == scalar:
                    candidates.append(ins.address)
                    break
    return candidates


def find_prologue_x86_64(data: bytes, addr: int, md: Cs) -> "Optional[int]":
    start = max(addr - Config.PROLOGUE_SEARCH_RANGE, 0)
    chunk = data[start: addr + 8]
    instrs = list(md.disasm(chunk, start))
    target_idx = next((i for i, ins in enumerate(instrs) if ins.address == addr), None)
    if target_idx is None:
        return None
    for idx in range(target_idx, max(target_idx - Config.MAX_INSTRS_BACKWARD, 0), -1):
        ins = instrs[idx]
        # x86_64 function prologue patterns
        is_prologue = (
            (ins.mnemonic == "push" and ins.op_str == "rbp")
            or (ins.mnemonic == "sub" and "rsp," in ins.op_str)
            or (ins.mnemonic == "push" and ins.op_str in ("r15", "r14", "r13", "r12", "rbx"))
        )
        if is_prologue:
            if idx == 0:
                return ins.address
            prev = instrs[idx - 1]
            if prev.mnemonic in ("ret", "jmp", "int3", "nop", "endbr64"):
                return ins.address
            # Also accept if previous is a return-like cc (0xcc = int3 padding)
            if prev.mnemonic == "int3":
                return ins.address
    return None


def analyze_function_x86_64(
    data: bytes, func_addr: int, text_offset: int, text_va: int
) -> tuple:
    file_off = text_offset + (func_addr - text_va)
    chunk = data[file_off: file_off + Config.ANALYSIS_CODE_SIZE]
    md = Cs(CS_ARCH_X86, CS_MODE_64)
    md.detail = False
    instrs = list(md.disasm(chunk, func_addr))
    saved = set()
    has_frame = False
    has_sp_adj = False
    callee_saved = {"rbx", "rbp", "r12", "r13", "r14", "r15"}
    for ins in instrs[: Config.ANALYSIS_INSTR_COUNT]:
        if ins.mnemonic == "push" and ins.op_str == "rbp":
            has_frame = True
        if ins.mnemonic == "sub" and "rsp," in ins.op_str:
            has_sp_adj = True
        if ins.mnemonic == "push" and ins.op_str in callee_saved:
            saved.add(ins.op_str)
        if ins.mnemonic in ("call",):
            break
    return saved, has_frame, has_sp_adj


def find_function_offset_x86_64(
    data: bytes, text: "object", image_base: int, error_line: int, verbose: bool
) -> "Optional[FunctionCandidate]":
    import bisect
    from capstone.x86_const import X86_OP_IMM

    text_va = text.virtual_address
    md = Cs(CS_ARCH_X86, CS_MODE_64)
    md.detail = True

    if verbose:
        print(f"[*] x86_64 forward scan: building func-start list + finding MOV imm={error_line} ...")

    func_starts = []
    mov_candidates = []
    prev_was_ret = True

    for off in range(text.offset, text.offset + text.size, Config.DISASM_CHUNK_SIZE):
        chunk_end = min(off + Config.DISASM_CHUNK_SIZE, text.offset + text.size)
        chunk = data[off:chunk_end]
        chunk_va = text_va + (off - text.offset)
        for ins in md.disasm(chunk, chunk_va):
            # track function starts (ret-only boundaries)
            if prev_was_ret and ins.mnemonic not in ("int3", "nop"):
                func_starts.append(ins.address)
                prev_was_ret = False
            if ins.mnemonic == "ret":
                prev_was_ret = True
            elif ins.mnemonic not in ("int3", "nop"):
                prev_was_ret = False

            # find MOV with exact immediate == error_line
            if ins.mnemonic in ("mov", "movabs", "movsx", "movzx"):
                for op in ins.operands:
                    if op.type == X86_OP_IMM and op.imm == error_line:
                        mov_candidates.append(ins.address)
                        break

    if verbose:
        print(f"[+] {len(func_starts)} function starts, {len(mov_candidates)} MOV-{error_line} candidates")

    results = []
    seen = set()
    for mov_addr in mov_candidates:
        idx = bisect.bisect_right(func_starts, mov_addr) - 1
        if idx < 0:
            continue
        func_va = func_starts[idx]
        if func_va in seen:
            continue
        seen.add(func_va)

        saved, has_frame, has_sp_adj = analyze_function_x86_64(
            data, func_va, text.offset, text_va
        )
        offset = func_va - image_base
        mov_offset = mov_addr - func_va
        c = FunctionCandidate(
            func_addr=func_va,
            offset=offset,
            saved_regs=saved,
            has_frame_setup=has_frame,
            has_sp_adjust=has_sp_adj,
            mov_offset=mov_offset,
        )
        results.append(c)
        if verbose:
            tag = "[std prologue]" if (has_frame and has_sp_adj) else ""
            print(f"    func=0x{func_va:08x}  offset=0x{offset:08x}  mov_delta={mov_offset}  {tag}")

    if not results:
        return None
    return max(results, key=lambda r: r.score)


