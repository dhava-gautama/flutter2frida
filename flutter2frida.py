@dataclasses.dataclass(frozen=True)
class FileInfo:
    name: str
    size: int
    sha256: str

    @property
    def size_formatted(self) -> str:
        sz = self.size
        for unit in ["B", "KB", "MB", "GB"]:
            if sz < 1024:
                return f"{sz:.2f} {unit}"
            sz /= 1024
        return f"{sz:.2f} TB"


@dataclasses.dataclass
class FlutterVersion:
    version: str
    engine_commit: str


@dataclasses.dataclass
class FunctionCandidate:
    func_addr: int
    offset: int
    saved_regs: set
    has_frame_setup: bool   # stp x29/x30 (arm64) or push rbp (x86_64)
    has_sp_adjust: bool     # sub sp (arm64) or sub rsp (x86_64)
    mov_offset: int

    @property
    def score(self) -> int:
        score = 0
        if self.has_frame_setup and self.has_sp_adjust:
            score += 20
        elif self.has_sp_adjust:
            score += 10
        if 100 <= self.mov_offset <= 500:
            score += 15
        if len(self.saved_regs) >= 1:
            score += 5
        return score


@dataclasses.dataclass
class ErrorLineInfo:
    func_start_line: "Optional[int]"
    error_line: "Optional[int]"


