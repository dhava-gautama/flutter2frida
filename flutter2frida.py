def run_analysis(libapp_path: str, libflutter_path: str, arch: str,
                 enginehash_path: "Optional[str]", output_path: str,
                 apk_info: "Optional[FileInfo]", verbose: bool,
                 proxy: "Optional[str]" = None,
                 cert: "Optional[str]" = None) -> int:

    # 1. Snapshot hash -> Flutter version
    print(f"\n[*] libapp:    {libapp_path}")
    print(f"[*] libflutter: {libflutter_path}")

    snapshot_hash = get_snapshot_hash(libapp_path)
    if not snapshot_hash:
        print("[!] Could not extract snapshot hash from libapp.so")
        return 1
    print(f"[+] snapshot hash: {snapshot_hash}")

    flutter_version = find_flutter_version(snapshot_hash, enginehash_path)
    if not flutter_version:
        print(f"[!] Version not found for hash {snapshot_hash}")
        print("    Rebuild enginehash.csv: https://github.com/Impact-I/reFlutter scripts/gen_enginehash.py")
        print("    Then pass with -e enginehash.csv")
        return 1
    print(f"[+] Flutter {flutter_version.version}  engine {flutter_version.engine_commit}")

    # 2. BoringSSL error line
    boringssl_rev = get_boringssl_revision(flutter_version.version)
    if not boringssl_rev:
        print("[!] Could not find BoringSSL revision in Flutter DEPS")
        return 1
    print(f"[+] BoringSSL rev: {boringssl_rev}")

    ssl_x509 = fetch_boringssl_file(boringssl_rev)
    if not ssl_x509:
        print("[!] Could not fetch ssl_x509.cc from BoringSSL")
        return 1

    error_info = find_error_line_number(ssl_x509)
    if not error_info.error_line:
        print("[!] OPENSSL_PUT_ERROR line not found in ssl_x509.cc")
        return 1
    print(f"[+] OPENSSL_PUT_ERROR line: {error_info.error_line}")

    # 3. Auto-detect arch if not forced
    if arch == "auto":
        arch = detect_arch(libflutter_path)
        print(f"[*] Detected arch: {arch}")

    # 4. Find function offset
    candidate = find_function_offset(libflutter_path, error_info.error_line, arch, verbose)
    if not candidate:
        print("[!] Could not locate target function in libflutter.so")
        return 1

    offset_str = f"0x{candidate.offset:08x}"
    print(f"\n[+] Function: 0x{candidate.func_addr:08x}")
    print(f"[+] Offset:   {offset_str}")
    print(f"[+] Score:    {candidate.score}")

    # 5. Generate script
    generate_frida_script(
        output_path=output_path,
        offset=candidate.offset,
        version=flutter_version.version,
        engine_commit=flutter_version.engine_commit,
        arch=arch,
        apk_info=apk_info,
        libapp_name=os.path.basename(libapp_path),
    )
    print(f"\n[+] Script: {output_path}")

    # Optional: generate config.js for native-connect-hook proxy redirection
    config_generated = False
    if proxy:
        if ":" in proxy:
            proxy_host, proxy_port_str = proxy.rsplit(":", 1)
            try:
                proxy_port = int(proxy_port_str)
            except ValueError:
                print(f"[!] Invalid proxy port in '{proxy}'")
                proxy_host, proxy_port = proxy, 8080
        else:
            proxy_host, proxy_port = proxy, 8080

        config_out = os.path.join(os.path.dirname(os.path.abspath(output_path)), "config.js")
        config_generated = generate_config_js(config_out, proxy_host, proxy_port, cert)
        if config_generated:
            print(f"[+] Config: {config_out}  (proxy={proxy_host}:{proxy_port})")

    hook_js = os.path.join(os.path.dirname(os.path.abspath(output_path)), "native-connect-hook.js")
    if config_generated and os.path.exists(hook_js):
        print(f"\n[+] Full intercept (spawn):")
        print(f"    frida -H 127.0.0.1:27042 -f <package> \\")
        print(f"        -l config.js -l native-connect-hook.js -l {os.path.basename(output_path)}")
    else:
        print(f"[+] TLS bypass only:")
        print(f"    frida -H 127.0.0.1:27042 -n <process> -l {output_path}")

    return 0


